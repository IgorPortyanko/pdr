function getSignCategoryId(number: string) {
  if (number.startsWith('1.')) return 'warning';
  if (number.startsWith('2.')) return 'priority';
  if (number.startsWith('3.')) return 'prohibit';
  if (number.startsWith('4.')) return 'mandate';
  if (number.startsWith('5.')) return 'info';
  if (number.startsWith('6.')) return 'service';
  if (number.startsWith('7.')) return 'tabl';

  return 'signs';
}

