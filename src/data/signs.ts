const collator = new Intl.Collator('uk-UA', {
  numeric: true,
  sensitivity: 'base',
});

export const SIGNS: Signs[] = Object.entries(signImages)
  .map(([path, src]) => {
    const match = path.match(/\/([\d.]+)\.png$/);
    const number = match?.[1];

    if (!number) {
      return null;
    }

    const details = signDetails[number];

    return {
      id: number,
      number,
      title: details?.title ?? `Дорожній знак ${number}`,
      desc: details?.desc ?? `Опис для знака ${number} не знайдено в офіційному тексті.`,
      src,
      categoryId: getSignCategoryId(number),
    };
  })
  .filter((sign): sign is Signs => sign !== null)
  .sort((a, b) => collator.compare(a.number, b.number))
  .map((sign, index) => ({
    ...sign,
    id: String(index + 1),
  }));
